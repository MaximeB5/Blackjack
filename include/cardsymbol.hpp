#ifndef CARDSYMBOL_H
#define CARDSYMBOL_H

/**
 * @brief enum class CardSymbol : int
 * It contains Heart, Tile, Clover and Pike.
 * 
 */
enum class CardSymbol : int {
    Heart   = 0,    // Red Color
    Tile    = 1,    // Red Color
    Clover  = 2,    // Black Color
    Pike    = 3     // Black Color
};

#endif // CARDSYMBOL_H